/// Константы и логика фильтров карты пруда.
/// Вынесены из pond_map_screen.dart для unit-тестирования.
/// ─────────────────────────────────────────────────────────
/// ⚠️ НЕ ИЗМЕНЯТЬ без обновления тестов в test/screens/pond_map_filter_test.dart.
///
/// Требования к dropdown фильтров:
///
///   1. Dropdown НИКОГДА не перекрывает нижнее меню
///   2. При пересечении с нижним меню — скрывается СТРОГО ПОД меню
///   3. ОБЯЗАТЕЛЬНЫЙ ТЕСТ: dropdown скрывается под нижним меню
///   4. Прикреплён к нижнему краю карточки, без зазора (gap = 0)
///   5. Углы: карточка — нижние выпрямляются, верхние неизменные;
///      dropdown — верхние выпрямлены (стыкуются с карточкой), нижние скруглены
///   6. Сворачивается: выбор опции, повторный тап, тап в пустую область
///   7. НИКОГДА не сворачивается при скролле — скроллится со страницей
///   8. НИКОГДА не блокирует скролл страницы
///   9. ВСЕГДА полная высота, НИКОГДА не меняет высоту
///  10. НИКОГДА не открывается вверх — только вниз. ОБЯЗАТЕЛЬНЫЙ ТЕСТ
///  11. НИКОГДА не сдвигает контент. ОБЯЗАТЕЛЬНЫЙ ТЕСТ
///  12. z-order: над контентом, ПОД нижним меню
///
/// ┌─────────────────────────────────────────────────────────────────┐
/// │ ТАБЛИЦА КРИТИЧЕСКИХ БАГОВ (НЕ ПОВТОРЯТЬ)                       │
/// ├─────────────────────────────────────────────────────────────────┤
/// │ # │ Баг                          │ Почему критический           │
/// ├───┼──────────────────────────────┼──────────────────────────────┤
/// │ 1 │ Контент двигается вниз       │ inline Stack двигает контент │
/// │ 2 │ Dropdown сжимается           │ пункты нечитаемые            │
/// │ 3 │ Dropdown закрывается         │ теряется контекст фильтрации │
/// │ 4 │ Dropdown летает при скролле  │ визуальный глитч             │
/// │ 5 │ Залезает за нижнее меню      │ нарушает z-order             │
/// │ 6 │ Скроллится внутрь            │ два скролла одновременно     │
/// │ 7 │ Зазор между кнопкой и списком│ визуальный разрыв            │
/// │ 8 │ Нижние углы кнопки круглые   │ визуальный баг при раскрытии │
/// └─────────────────────────────────────────────────────────────────┘

